/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	static uint8_t Count_10ms;
	static uint8_t Count_200ms;
	static uint8_t led_state=0;//记录上一次led的状态
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
	//10ms执行一次按键检测
	if(++ Count_10ms == 10)
	{
		Count_10ms = 0;
		KeyScan();
	}
	//200ms操作一次led
	if(++Count_200ms == 200)
	{
		Count_200ms=0;
		//开启LED控制
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, GPIO_PIN_SET);
		//关闭所有LED
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8 |  GPIO_PIN_9 |  GPIO_PIN_10 |  GPIO_PIN_11 |  GPIO_PIN_12 |  GPIO_PIN_13 |  GPIO_PIN_14 |  GPIO_PIN_15, GPIO_PIN_SET);
		++ led_state;
		led_state %=2;
		switch(Vlot_State)
		{
			case 0://超过上限
				if(led_state == 0) HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8 << ((uint8_t)Upper_Led-1), GPIO_PIN_RESET);
				else HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8 << ((uint8_t)Upper_Led-1), GPIO_PIN_SET);
				break;
			case 1://低于下限
				if(led_state == 0) HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8 << ((uint8_t)Lower_Led-1), GPIO_PIN_RESET);
				else HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8 << ((uint8_t)Lower_Led-1), GPIO_PIN_SET);
				break;
			case 2://正常LED熄灭
				CloseLed();
				break;
			default: break;
		}
		//关闭LED控制
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, GPIO_PIN_RESET);
	}
		
  /* USER CODE END SysTick_IRQn 1 */
}

